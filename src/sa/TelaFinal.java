    private void confBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_confBtnActionPerformed
        try{
            String inputCnh = cnh.getText().toUpperCase();
            int inputIdade = Integer.parseInt(idade.getText());
     
            aviso.setText("");
            aviso2.setText("");

            if(!("B".equals(inputCnh)|| "C".equals(inputCnh) || "D".equals(inputCnh) || "E".equals(inputCnh))){
                aviso.setText("Sua categoria deve ser acima de B \n");
                return;
            }

            if(inputIdade < 18){
                aviso2.setText("Voc√™ deve ter idade maior de 18");
                return;
            }
            String show =
                    "SEUS DADOS\n\nNome: " + nome.getText() +
                    "\nTelefone: " + telefone.getText();
            
                show = show+"\n\nDADOS DO VE√çCULO\n";
               show = show + "\nModelo: " + carro_select.getModelo(); 
               show = show + "\nMarca: " + carro_select.getMarca(); 
               show = show + "\nAno: " + carro_select.getAno(); 
               
               if (Cliente.getCompra() == 0){
                show = show + "\nValor Total de Di√°rias: R$" + String.valueOf(carro_select.getPrecoAluguel() * Cliente.getQtd_dias());
            } else if (Cliente.getCompra() == 1){
                show = show + "\nValor Total da Compra: R$" + String.valueOf(carro_select.getPrecoCompra());
            }
               
               show = show + "\n\nO nosso Atendente entrar√° em contato para finalizar seu Atendimento.\nA equipe M√£o na Roda agradece pela prefer√™ncia!";
                      
               System.out.println(carro_select.getPrecoCompra());
            JOptionPane.showMessageDialog(
                    null,
                    show,
                    "Dados Finais",
                    JOptionPane.INFORMATION_MESSAGE
            );
            
        } catch (NumberFormatException erro) {
            aviso.setText("Digite n√∫meros v√°lidos");
        }
        
        
    }//GEN-LAST:event_confBtnActionPerformed

